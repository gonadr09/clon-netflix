export const getMovieTrailer = async (id) => {
  const res = await axios.get(TMDB.path.movies.baseURL + id + '/videos?api_key=' + TMDB.api_key + '&language=en-US')
  const trailer = await res.data.results.find(result => result.type == "Trailer" && result.site == "YouTube")
  const youtubeUrl = "https://www.youtube.com/embed/" + trailer.key
  return youtubeUrl 
};

